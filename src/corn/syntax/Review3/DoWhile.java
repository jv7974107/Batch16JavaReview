package corn.syntax.Review3;

public class DoWhile {
    public static void main(String[] args) {
        int a=10;

        do {
            System.out.println("Bye");
            a--;
        }while (a>6);

        System.out.println("----------------");
        int b=4;

        while (b>6){
            System.out.println("Bye");
        }

    }
}
